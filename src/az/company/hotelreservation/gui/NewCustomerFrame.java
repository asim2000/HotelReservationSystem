    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
         try {
           List<Address> countries = addressService.getAddressesByParent(null);
            for(Address country:countries){
                countryCb.addItem(country.getName());
            }
            int index = countryCb.getSelectedIndex();
            Address country = countries.get(index);
            List<Address> cities = addressService.getAddressesByParent(country.getId());
            for(Address city:cities){
                cityCb.addItem(city.getName());
            }
            index = cityCb.getSelectedIndex();
            Address city = cities.get(index);
            List<Address> regions = addressService.getAddressesByParent(city.getId());
            for(Address region:regions){
                regionCb.addItem(region.getName());
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewCustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(NewCustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

