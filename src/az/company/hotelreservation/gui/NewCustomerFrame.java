    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Customer customer = new Customer();
            customer.setName(nameTxt.getText());
            customer.setSurname(surnameTxt.getText());
            customer.setPassword(passwordTxt.getText());
            customer.setDateOfBirth(dobJdc.getDate());
            customer.setGender(Gender.valueOf(genderCb.getSelectedItem().toString()));
            customer.setImage("image.jpg");
            
            Contact contact = new Contact();
            contact.setEmail(emailTxt.getText());
            contact.setPhone(phoneTxt.getText());
            
            customer.setContact(contact);
            
            Address address = new Address();
            address.setName(addressLineTxt.getText());
            List<Address> countries = addressService.getAddressesByParent(null);
            List<Address> cities = addressService.getAddressesByParent(countries.get(countryCb.getSelectedIndex()).getId());
            List<Address> regions = addressService.getAddressesByParent(cities.get(cityCb.getSelectedIndex()).getId());
            address.setParentId(regions.get(regionCb.getSelectedIndex()).getId());
            customer.setAddress(address);
            
            this.customerService.create(customer);
            
            JOptionPane.showMessageDialog(null, "Customer has been successfully added!");
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Problem! Customer has not been successfully added!","Error",JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_saveBtnActionPerformed

